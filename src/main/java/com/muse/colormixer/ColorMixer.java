package com.muse.colormixer;

// Model class
public class ColorMixer {

    // Create variables to store colors
    private int mRed;
    private int mGreen;
    private int mBlue;

    // Create getters and setters for each color

    public void setRed(int red) {
        mRed = red;
    }

    public int getRed() {
        return mRed;
    }

    public void setGreen(int green) {
        mGreen = green;
    }

    public int getGreen() {
        return mGreen;
    }

    public void setBlue(int blue) {
        mBlue = blue;
    }

    public int getBlue() {
        return mBlue;
    }



}
